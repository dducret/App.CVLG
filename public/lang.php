function supported_languages(): array
{
    return [
        'fr' => 'Francais',
        'en' => 'English',
        'de' => 'Deutsch',
        'es' => 'Espanol',
        'it' => 'Italiano',
    ];
}

function normalize_language(?string $language, string $fallback = 'fr'): string
{
    $language = strtolower(trim((string) $language));
    return array_key_exists($language, supported_languages()) ? $language : $fallback;
}

function current_lang(): string
{
    global $lang;
    return $lang;
}

function set_lang(?string $language): string
{
    global $lang;
    $lang = normalize_language($language);
    $_SESSION['lang'] = $lang;

    return $lang;
}

